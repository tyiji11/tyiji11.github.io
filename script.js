function fillList(el, items) {
  el.innerHTML = items
    .map(
      (item, index) =>
        `<li class="motion-item" style="--delay:${index * 70}ms">${item}</li>`,
    )
    .join("");
}

function fillJobs(el, jobs) {
  el.innerHTML = jobs
    .map(
      (job, index) =>
        `<article class="job motion-item" style="--delay:${index * 90}ms"><h3 class="job-title">${job.title}</h3><p class="job-desc">${job.desc}</p></article>`,
    )
    .join("");
}

function setLang(langKey) {
  const data = content[langKey];

  document.documentElement.lang = data.lang;
  document.title = data.pageTitle;

  nodes.name.textContent = data.name;
  nodes.role.textContent = data.role;
  nodes.tagline.textContent = data.tagline;

  nodes.m1n.textContent = data.meta[0].name;
  nodes.m1v.textContent = data.meta[0].value;
  nodes.m2n.textContent = data.meta[1].name;
  nodes.m2v.textContent = data.meta[1].value;
  nodes.m3n.textContent = data.meta[2].name;
  nodes.m3v.textContent = data.meta[2].value;

  nodes.contactTitle.textContent = data.contactTitle;
  nodes.c1.textContent = data.contact[0];
  nodes.c2.textContent = data.contact[1];
  nodes.c3.textContent = data.contact[2];
  nodes.c4.textContent = data.contact[3];

  nodes.skillsTitle.textContent = data.skillsTitle;
  fillList(nodes.skillsList, data.skills);

  nodes.eduTitle.textContent = data.eduTitle;
  fillList(nodes.eduList, data.education);

  nodes.prefTitle.textContent = data.prefTitle;
  fillList(nodes.prefList, data.preferences);

  nodes.expTitle.textContent = data.expTitle;
  fillJobs(nodes.jobs, data.jobs);

  nodes.projTitle.textContent = data.projTitle;
  fillList(nodes.projList, data.projects);

  nodes.footer.textContent = data.footer;

  const isZh = langKey === "zh";
  nodes.langZh.classList.toggle("active", isZh);
  nodes.langEn.classList.toggle("active", !isZh);
  nodes.langZh.setAttribute("aria-selected", String(isZh));
  nodes.langEn.setAttribute("aria-selected", String(!isZh));
}

nodes.langZh.addEventListener("click", () => setLang("zh"));
nodes.langEn.addEventListener("click", () => setLang("en"));

setLang("zh");
